package sample;

public class IPModel {
    private String ipNetwork;
    private String netmask;
    private String ipBinary;
    private String subnetBinary;

    public String getIpNetwork() {
        return ipNetwork;
    }

    public void setIpNetwork(String ipNetwork) {
        this.ipNetwork = ipNetwork;
    }

    public String getNetmask() {
        return netmask;
    }

    public void setNetmask(String netmask) {
        this.netmask = netmask;
    }

    public String getIpBinary() {
        return ipBinary;
    }

    public void setIpBinary(String ipBinary) {
        this.ipBinary = ipBinary;
    }

    public String getSubnetBinary() {
        return subnetBinary;
    }

    public void setSubnetBinary(String subnetBinary) {
        this.subnetBinary = subnetBinary;
    }
}
